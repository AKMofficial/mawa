/**
 * Get all nationalities in Arabic
 * @returns Array of all nationalities in Arabic
 */
export function getAllNationalities(): string[] {
  return [
    ...arabCountries,
    ...asianCountries,
    ...europeanCountries,
    ...africanCountries,
    ...northAmericanCountries,
    ...southAmericanCountries,
    ...oceaniaCountries,
  ].sort((a, b) => a.localeCompare(b, 'ar'));
}

/**
 * Get nationalities by region
 * @param region - The region to filter by
 * @returns Array of nationalities for the specified region
 */
export function getNationalitiesByRegion(region: 'arab' | 'asian' | 'european' | 'african' | 'northAmerican' | 'southAmerican' | 'oceania'): string[] {
  const regionMap = {
    arab: arabCountries,
    asian: asianCountries,
    european: europeanCountries,
    african: africanCountries,
    northAmerican: northAmericanCountries,
    southAmerican: southAmericanCountries,
    oceania: oceaniaCountries,
  };
  
  return regionMap[region].sort((a, b) => a.localeCompare(b, 'ar'));
}

/**
 * Search nationalities by query
 * @param query - Search query in Arabic
 * @returns Array of matching nationalities
 */
export function searchNationalities(query: string): string[] {
  if (!query.trim()) {
    return getAllNationalities();
  }
  
  const searchTerm = query.toLowerCase().trim();
  return getAllNationalities().filter(nationality =>
    nationality.toLowerCase().includes(searchTerm)
  );
}

/**
 * Get nationality options for select components
 * @returns Array of option objects with value and label
 */
export function getNationalityOptions(): { value: string; label: string }[] {
  return getAllNationalities().map(nationality => ({
    value: nationality,
    label: nationality,
  }));
}